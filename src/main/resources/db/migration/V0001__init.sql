CREATE TABLE users
(
  id Serial PRIMARY KEY NOT NULL,
  login VARCHAR(255) NOT NULL,
  password VARCHAR(255) NOT NULL
);
CREATE TABLE usersdata
(
  rating INTEGER DEFAULT 0 NOT NULL,
  game_count INTEGER DEFAULT 0 NOT NULL,
  game_count_win INTEGER DEFAULT 0 NOT NULL,
  crystal_green INTEGER DEFAULT 0 NOT NULL,
  crystal_red INTEGER DEFAULT 0 NOT NULL,
  crystal_blue INTEGER DEFAULT 0 NOT NULL,
  crystal_purple INTEGER DEFAULT 0 NOT NULL,
  login VARCHAR(255) PRIMARY KEY NOT NULL
);